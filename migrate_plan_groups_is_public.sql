-- 用途：总套餐关闭展示后，该总套餐及下所有子套餐不在用户端套餐列表显示
-- 执行前：建议先备份数据库。若已执行过会报「列已存在」，可忽略。
-- =============================================================================

ALTER TABLE `plan_groups`
  ADD COLUMN `is_public` TINYINT(1) NOT NULL DEFAULT 1
  COMMENT '是否在套餐列表展示：1=展示，0=不展示'
  AFTER `status`;
